/**
 * Function: createUniqueSet
 * Description: This function accepts an array as input and generates a Set with its unique elements.
 * Parameters:
 *   - inputArray: The array used to create the Set
 * Returns:
 *   - A Set comprising unique elements from the provided array
 */
const createUniqueSet = (inputArray) => new Set(inputArray);

export default createUniqueSet; // Exports the createUniqueSet function
